# pragma once
# include <Siv3D.hpp>
# include <memory>

//------------------------------------------------------------
// アプリ全体で使用する定数
//------------------------------------------------------------

// ゲームの地面のサイズ（正方形の一辺の長さ）
inline constexpr double GroundSize = 100.0;

//------------------------------------------------------------
// ゲーム内で使用する状態の定義
//------------------------------------------------------------

// ゲーム全体の状態を表す列挙型
enum class GameState {
	Title,       // タイトル画面
	Play,        // ゲームプレイ中
	GameOver,    // ゲームオーバー画面
	GameClear    // ゲームクリア画面
};

// プレイヤーの状態を表す列挙型
enum class PlayerState {
	Idle,        // 待機中
	Slash,       // 近接攻撃中（斬撃）
	Shot,        // 遠距離攻撃中（射撃）
	Dodge,       // 回避中
	Dead         // 死亡
};

// 敵の状態を表す列挙型
enum class EnemyState {
	Chase,       // プレイヤーを追跡中
	Attack,      // 攻撃中
	Recover      // 攻撃後の回復中（待機）
};

//------------------------------------------------------------
// QA・デバッグ用のシステム設定（グローバルに管理）
//------------------------------------------------------------

// デバッグやQA（品質保証）用の設定を管理する構造体
struct SystemConfig {
	// デバッグパネルの表示ON/OFF（初期値はOFF）
	bool showDebugPanel = false;

	// 当たり判定の可視化ON/OFF（初期値はOFF）
	bool showHitboxes = false;

	// ゲーム全体の時間の進み具合（スローモーション用、1.0が通常速度）
	double timeScale = 1.0;

	// コマ送りモードON/OFF（初期値はOFF）
	bool stepMode = false;

	// コマ送りモードで1フレーム進めるかどうか（初期値は進めない）
	bool stepNext = false;

	// 無敵モードON/OFF（初期値はOFF）
	bool invincibleMode = false;

	// ヒットストップ（時間停止）のタイマー（秒単位）
	double hitStopTimer = 0.0;

	//------------------------------------------------------------
	// 実際のDeltaTime（フレーム間の経過時間）を取得
	// ヒットストップ中やコマ送り中は0になる
	// @return DeltaTime（秒）
	//------------------------------------------------------------
	double getDeltaTime() const {
		if (hitStopTimer > 0.0) return 0.0; // ヒットストップ中は時間を進めない
		if (stepMode) return stepNext ? (1.0 / 60.0) : 0.0; // コマ送りモードの場合
		return Scene::DeltaTime() * timeScale; // 通常のDeltaTimeにtimeScaleを適用
	}

	//------------------------------------------------------------
	// ヒットストップ（手触り向上のための一時停止）の追加
	// @param seconds: ヒットストップの持続時間（秒）
	//------------------------------------------------------------
	void addHitStop(double seconds) {
		hitStopTimer = seconds;
	}

	//------------------------------------------------------------
	// システムの更新処理
	// 実時間でタイマーを減らす
	//------------------------------------------------------------
	void update() {
		if (hitStopTimer > 0.0) {
			hitStopTimer -= Scene::DeltaTime(); // ヒットストップタイマーを減少
		}
		stepNext = false; // コマ送りモードの進行フラグをリセット
	}
};

//------------------------------------------------------------
// シングルトン的に扱うためのグローバル変数
// アプリ全体でSystemConfigを共有するために使用
//------------------------------------------------------------
inline SystemConfig g_config;
