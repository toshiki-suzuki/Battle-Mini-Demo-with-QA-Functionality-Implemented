# pragma once
# include "Common.hpp"

//------------------------------------------------------------
// プレイヤークラス
// プレイヤーキャラクターの状態や挙動を管理するクラス
//------------------------------------------------------------
class IAttack; // 攻撃クラスのインターフェースを前方宣言

class Player {
public:
	//------------------------------------------------------------
	// コンストラクタ・デストラクタ
	//------------------------------------------------------------
	Player();  // プレイヤーオブジェクトの初期化
	~Player(); // プレイヤーオブジェクトの破棄

	//------------------------------------------------------------
	// 更新処理
	// プレイヤーの状態を更新します。
	// @param camera: 現在のカメラ情報
	//------------------------------------------------------------
	void update(const BasicCamera3D& camera);

	//------------------------------------------------------------
	// 描画処理
	// プレイヤーを描画します。
	//------------------------------------------------------------
	void draw() const;

	//------------------------------------------------------------
	// デバッグ描画処理
	// プレイヤーのデバッグ情報を描画します。
	// @param font: 使用するフォント
	//------------------------------------------------------------
	void drawDebug(const Font& font) const;

	//------------------------------------------------------------
	// 被弾処理
	// プレイヤーがダメージを受けた際の処理を行います。
	// @param sourcePos: 攻撃元の座標（ノックバック方向計算用）
	// @param damage: 受けるダメージ量
	// @param sourceName: 攻撃元の名前（デバッグ用）
	//------------------------------------------------------------
	void takeDamage(const Vec3& sourcePos, int32 damage, const String& sourceName);

	//------------------------------------------------------------
	// リセット処理
	// プレイヤーの状態を初期化します。
	//------------------------------------------------------------
	void reset();

	//------------------------------------------------------------
	// 状態取得
	//------------------------------------------------------------
	// プレイヤーが生存しているかどうかを返します。
	// @return true: 生存, false: 死亡
	bool isAlive() const { return m_hp > 0; }

	// プレイヤーが無敵状態かどうかを返します。
	// @return true: 無敵, false: 通常
	bool isInvincible() const { return m_invincibleTimer > 0 || m_dodgeInvincibleTimer > 0; }

	// プレイヤーの現在位置を取得します。
	// @return 現在位置（Vec3）
	Vec3 getPos() const { return m_pos; }

	// プレイヤーの当たり判定（Hurtbox）を取得します。
	// @return 当たり判定（Sphere）
	Sphere getHurtbox() const { return Sphere{ m_pos, 1.0 }; }

	// プレイヤーの攻撃リストを取得します。
	// @return 攻撃リスト（Array<std::unique_ptr<IAttack>>）
	const Array<std::unique_ptr<IAttack>>& getAttacks() const { return m_attacks; }

private:
	//------------------------------------------------------------
	// 入力処理
	// プレイヤーの移動やアクション入力を処理します。
	//------------------------------------------------------------
	void handleInput();

	//------------------------------------------------------------
	// 状態更新処理
	// プレイヤーの現在の状態を更新します。
	//------------------------------------------------------------
	void updateState();

	//------------------------------------------------------------
	// 状態変更処理
	// プレイヤーの状態を変更し、必要な初期化を行います。
	// @param nextState: 次の状態
	//------------------------------------------------------------
	void changeState(PlayerState nextState);

	//------------------------------------------------------------
	// メンバ変数
	//------------------------------------------------------------

	// 現在の座標
	Vec3 m_pos = { 0, 1, 0 };

	// 現在のHPと最大HP
	int32 m_hp = 5;
	int32 m_maxHp = 5;

	// 向きと移動方向
	double m_angle = 0.0;         // プレイヤーの向き（ラジアン）
	Vec3 m_moveDir{ 0, 0, 1 };   // 移動方向
	Vec3 m_aimDir{ 0, 0, 1 };    // 照準方向
	Vec3 m_knockbackVel{ 0, 0, 0 }; // ノックバック速度

	// 状態管理
	PlayerState m_state = PlayerState::Idle; // 現在の状態
	double m_stateTimer = 0.0;               // 状態の経過時間
	Optional<PlayerState> m_inputBuffer;     // 入力バッファ

	// 回避（Dodge）関連
	double m_dodgeInvincibleTimer = 0.0; // 回避中の無敵時間
	double m_dodgeCooldown = 0.0;        // 回避のクールダウン時間

	// 被弾無敵
	double m_invincibleTimer = 0.0; // 被弾後の無敵時間

	// QAデバッグ用記録
	String m_lastHitBy = U"None"; // 最後に攻撃してきた相手の名前
	int32 m_lastDamage = 0;       // 最後に受けたダメージ量

	// 攻撃リスト
	Array<std::unique_ptr<IAttack>> m_attacks; // プレイヤーの攻撃オブジェクト
};
